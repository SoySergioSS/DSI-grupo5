    public void MostrarPanel(JPanel p){
        if (contenido.getComponentCount() > 0) {
            historial.push((JPanel) contenido.getComponent(0));
        }

        p.setSize(495, 637);
        p.setLocation(0, 0);
        
        contenido.removeAll();
        contenido.add(p, BorderLayout.CENTER);
        contenido.revalidate();
        contenido.repaint();

    }
    public void RetrocederPanel() {
        if (!historial.isEmpty()) {
            JPanel anterior = historial.pop(); // Obtener el Ãºltimo panel del historial
            contenido.removeAll();
            contenido.add(anterior, BorderLayout.CENTER);
            contenido.revalidate();
            contenido.repaint();
        } else {
            JOptionPane.showMessageDialog(this, "No hay panel anterior para mostrar.");
        }
    }
    

